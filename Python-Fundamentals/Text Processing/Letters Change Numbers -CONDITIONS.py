# Затова той любезно ви моли да напишете програма, която изчислява сумата на всички числа,
# след като са извършени операциите върху всяко число.

# Например, дадена ви е последователността "A12b s17G":
# Имаме два низа - "A12b" и "s17G".
# Извършваме операциите върху всяка от тях и ги сумираме.

# Започваме с буквата преди числото в първия низ.
# А е главна буква и позицията ѝ в азбуката е 1.
# Така че разделяме числото 12 с позицията 1 (12/1 = 12).

# След това преминаваме към буквата след числото.
#    b е малка буква и позицията ѝ е 2. Така че добавяме 2 към полученото число (12+2=14).
#   По същия начин за втория низ s е малка буква и позицията ѝ е 19, така че я умножаваме с числото (17*19 = 323).
#   След това имаме главна буква G с позиция 7, така че я изваждаме от полученото число (323 - 7 = 316).
#   Накрая събираме двата резултата и получаваме 14 + 316=330.

