# - Ако тя е главна, разделяте числото на позицията на буквата в азбуката.
# - Ако тя е малка, умножавате числото с позицията на буквата в азбуката.

# След това преминавате към буквата след числото.
# - Ако тя е главна, изваждате нейната позиция от полученото число.
# - Ако е малка буква, прибавяте нейната позиция към полученото число.
